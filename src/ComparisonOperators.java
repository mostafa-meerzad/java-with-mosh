public class ComparisonOperators {
    public static void main(String[] args) {
        int x = 10;
        int y = 1;

        System.out.print("x == y: ");
        System.out.println(x == y);
        System.out.print("x <= y: ");
        System.out.println(x <= y);
        System.out.print("x >= y: ");
        System.out.println(x >= y);
        System.out.print("x != y: ");
        System.out.println(x != y);
    }
}
